package exception;

public class EditoraDaoException extends GenericException {

	private static final long serialVersionUID = -7306165201428574472L;

	public EditoraDaoException() {
		super();
	}

	public EditoraDaoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EditoraDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public EditoraDaoException(String message) {
		super(message);
	}

	public EditoraDaoException(Throwable cause) {
		super(cause);
	}
	
	

}
